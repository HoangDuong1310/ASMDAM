    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        MauSacDTO newMauSac = getProductFromInput();
        if (mauSacService.creatNewMauSac(newMauSac) != null) {
            JOptionPane.showMessageDialog(this, "thanh cong");
        } else {
            JOptionPane.showConfirmDialog(this, "Thanh Cong");
        }
        loadTable();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int row = this.jTable1.getSelectedRow();
        if (row == -1) {
            return;
        }
        MauSacDTO updateProduct = getProductFromInput();
        String updateMauSacID = getMauSacIDFormSelectRow();
        updateProduct.setId(updateMauSacID);

        if (mauSacService.updateMauSacById(updateProduct) != null) {
            JOptionPane.showMessageDialog(this, "Thanh Cong");
        } else {
            JOptionPane.showMessageDialog(this, "that bai");
        }
        loadTable();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String selectID = getMauSacIDFormSelectRow();
        if (mauSacService.deleteMauSacById(selectID)) {
            JOptionPane.showMessageDialog(this, "thanh cong");
        } else {
            JOptionPane.showMessageDialog(this, "that bai");
        }
        loadTable();
    }//GEN-LAST:event_jButton3ActionPerformed

