    private void loadTable() {
        List<MauSacDTO> list = mauSacService.getMauSac();
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        for (MauSacDTO mauSac : list) {
            Object[] rowData = {
                mauSac.getId(),
                mauSac.getMa(),
                mauSac.getTen()
            };
            dtm.addRow(rowData);
        }
    }

    private String getMauSacIDFormSelectRow() {
        int selectRowIndex = jTable1.getSelectedRow();
        return jTable1.getValueAt(selectRowIndex, 0).toString();
    }

    private MauSacDTO getProductFromInput() {
        MauSacDTO fromMauSac = new MauSacDTO();

        String mauSacTen = txtTen.getText();
        fromMauSac.setTen(mauSacTen);

        String mauSacMa = txtMa.getText();
        fromMauSac.setMa(mauSacMa);

        return fromMauSac;
    }

