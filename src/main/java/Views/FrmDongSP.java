    public void loadTable() {
        List<DongSPDTO> list = service.getAll();
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        for (DongSPDTO dongSPDTO : list) {
            Object[] rowData = {
                dongSPDTO.getId(),
                dongSPDTO.getMa(),
                dongSPDTO.getTen()
            };
            dtm.addRow(rowData);
        }
    }

    private String getIDSelectRow() {
        int selectRow = jTable1.getSelectedRow();
        return jTable1.getValueAt(selectRow, 0).toString();
    }

    private DongSPDTO getInput() {
        DongSPDTO dongSPDTO = new DongSPDTO();
        String ma = txtnputMa.getText();
        String ten = txtInputTen.getText();

        dongSPDTO.setMa(ma);
        dongSPDTO.setTen(ten);
        return dongSPDTO;
    }
