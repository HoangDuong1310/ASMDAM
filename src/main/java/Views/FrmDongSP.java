    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        DongSPDTO dto = getInput();
        if (service.save(dto) != null) {
            JOptionPane.showMessageDialog(this, "Thành Công");
        } else {
            JOptionPane.showMessageDialog(this, "thật bại");
        }
        loadTable();
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int row = this.jTable1.getSelectedRow();
        if (row == -1) {
            return;
        }
        DongSPDTO update = getInput();
        String id = getIDSelectRow();
        update.setId(id);
        if (service.update(update) != null) {
            JOptionPane.showMessageDialog(this, "thanh cong");
        } else {
            JOptionPane.showMessageDialog(this, "that bai");
        }
        loadTable();
    }//GEN-LAST:event_btnSuaActionPerformed

    private void jTable1MouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseEntered
        int row = this.jTable1.getSelectedRow();
        if (row == -1) {
            return;
        }
        String ma = this.jTable1.getValueAt(row, 1).toString();
        String ten = this.jTable1.getValueAt(row, 2).toString();
        this.txtnputMa.setText(ma);
        this.txtInputTen.setText(ten);
    }//GEN-LAST:event_jTable1MouseEntered

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int row = this.jTable1.getSelectedRow();
        if (row == -1) {
            return;
        }
        String id = getIDSelectRow();
        service.delete(id);
        loadTable();
    }//GEN-LAST:event_btnXoaActionPerformed

