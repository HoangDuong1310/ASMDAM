    private void loadTable() {
        List<CuaHangDTO> list = cuaHangService.getCuaHang();
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        for (CuaHangDTO cuaHangDTO : list) {
            Object[] rowData = {
                cuaHangDTO.getId(),
                cuaHangDTO.getMa(),
                cuaHangDTO.getTen(),
                cuaHangDTO.getDiaChi(),
                cuaHangDTO.getThanhPho(),
                cuaHangDTO.getQuocGia()
            };
            dtm.addRow(rowData);
        }
    }

    private String getCuaHangIDFormSelectRow() {
        int selectRow = jTable1.getSelectedRow();
        return jTable1.getValueAt(selectRow, 0).toString();
    }

    private CuaHangDTO getCuaHangFormInput() {
        CuaHangDTO cuaHangDTO = new CuaHangDTO();
        String ma = txtMa.getText();
        String ten = txtTen.getText();
        String diaChi = txtDiaChi.getText();
        String thanhPho = txtThanhPho.getText();
        String quocGia = txtQuocGia.getText();

        cuaHangDTO.setMa(ma);
        cuaHangDTO.setTen(ten);
        cuaHangDTO.setDiaChi(diaChi);
        cuaHangDTO.setThanhPho(thanhPho);
        cuaHangDTO.setQuocGia(quocGia);
        return cuaHangDTO;
    }
