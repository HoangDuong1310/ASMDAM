    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        CuaHangDTO createCuaHang = getCuaHangFormInput();
        if (cuaHangService.creatNewCuaHang(createCuaHang) != null) {
            JOptionPane.showMessageDialog(this, "Thanh Cong");
        } else {
            JOptionPane.showMessageDialog(this, "that bai");
        }
        loadTable();

    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int row = this.jTable1.getSelectedRow();
        if (row == -1) {
            return;
        }
        CuaHangDTO updateCuaHang = getCuaHangFormInput();
        String updatCuaHangID = getCuaHangIDFormSelectRow();
        updateCuaHang.setId(updatCuaHangID);
        if (cuaHangService.updateCuaHangById(updateCuaHang) != null) {
            JOptionPane.showMessageDialog(this, "Thanh Cong");
        } else {
            JOptionPane.showMessageDialog(this, "that bai");
        }
        loadTable();
    }//GEN-LAST:event_btnSuaActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = this.jTable1.getSelectedRow();
        if (row == -1) {
            return;
        }
        String ma = this.jTable1.getValueAt(row, 1).toString();
        String ten = this.jTable1.getValueAt(row, 2).toString();
        String DiaChi = this.jTable1.getValueAt(row, 3).toString();
        String ThanhPho = this.jTable1.getValueAt(row, 4).toString();
        String QuocGia = this.jTable1.getValueAt(row, 5).toString();

        this.txtMa.setText(ma);
        this.txtTen.setText(ten);
        this.txtDiaChi.setText(DiaChi);
        this.txtThanhPho.setText(ThanhPho);
        this.txtQuocGia.setText(QuocGia);

    }//GEN-LAST:event_jTable1MouseClicked

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int row = this.jTable1.getSelectedRow();
        if (row == -1) {
            return;
        }
        String id = getCuaHangIDFormSelectRow();
        if (cuaHangService.deleteCuaHangById(id)) {
            JOptionPane.showMessageDialog(this, "thanh cong");

        } else {
            JOptionPane.showMessageDialog(this, "that bai");
        }
        loadTable();
    }//GEN-LAST:event_btnXoaActionPerformed

