    private void loadTable() {
        List<FromProduct> ds = _iManageProductService.getProducts();
        DefaultTableModel dtm = (DefaultTableModel) this.jTable1.getModel();
        dtm.setRowCount(0);
        for (FromProduct product : ds) {
            Object[] rowData = {
                product.getId(),
                product.getMa(),
                product.getTen()
            };

            dtm.addRow(rowData);
        }
    }

    private FromProduct getProductFromInput() {
        FromProduct fromProduct = new FromProduct();

        String productName = txtInputName.getText();
        fromProduct.setTen(productName);

        String productMa = txtInputMa.getText();
        fromProduct.setMa(productMa);

        return fromProduct;
    }

    private String getProductIDFromSelectedRow() {
        int selectedRowIndex = jTable1.getSelectedRow();
        return jTable1.getValueAt(selectedRowIndex, 0).toString();
    }

