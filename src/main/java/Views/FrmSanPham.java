    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        FromProduct newProduct = getProductFromInput();
        if (_iManageProductService.createNewProduct(newProduct) != null) {
            JOptionPane.showMessageDialog(this, "Thanh Cong");
        } else {
            JOptionPane.showMessageDialog(this, "That Bais");
        }
        loadTable();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int row = this.jTable1.getSelectedRow();
        if (row == -1) {
            return;
        }
        FromProduct updateProduct = getProductFromInput();
        String updateProductId = getProductIDFromSelectedRow();
        updateProduct.setId(updateProductId);
        if (_iManageProductService.updateProductById(updateProduct) != null) {
            JOptionPane.showMessageDialog(this, "thanhcong");
        } else {
            JOptionPane.showMessageDialog(this, "thatbai");
        }
        loadTable();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String selectedId = getProductIDFromSelectedRow();
        if (_iManageProductService.deleteProductById(selectedId)) {
            JOptionPane.showMessageDialog(this, "thanhcong");
        } else {
            JOptionPane.showMessageDialog(this, "thatbai");
        }
        loadTable();
    }//GEN-LAST:event_jButton3ActionPerformed

